namespace PatternsCore.FrameWorkStyleFactory.AbstractFactory
{
    public interface IDough
    {
        string ToString();
    }
    public class NyDough : IDough
    {
        public override string ToString()
        {
            return "extra thin dough";
        }
    }
    public class ChicagoDough : IDough
    {
        public override string ToString()
        {
            return "Thick dough";
        }
    }
}