using System;

namespace PatternsCore.Adapter
{
    public interface IDuck
    {
        void Quack();
        void Fly();
    }


    public class MallardDuck : IDuck
    {
        public void Quack()
        { Console.WriteLine("I am Quacking "); }

        public void Fly()
        { Console.WriteLine("I am Flying"); }
    }
}